    /// <summary>
    /// 将 SDK 错误码解码为中文描述，格式：[错误码 N] 描述
    /// </summary>
    public static string GetMessage(uint errorCode)
    {
        if (Messages.TryGetValue(errorCode, out var msg))
            return $"[{errorCode}] {msg}";
        return $"[{errorCode}] 未知错误";
    }

    /// <summary>
    /// 调用 NET_DVR_GetLastError 并返回解码后的字符串
    /// </summary>
    public static string GetLastError()
        => GetMessage(HCNetSDKApi.NET_DVR_GetLastError());
